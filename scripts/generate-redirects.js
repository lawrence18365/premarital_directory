const SPECIALTIES = [
  'christian',
  'catholic',
  'lgbtq',
  'online',
  'gottman',
  'prepare-enrich',
  'interfaith',
  'second-marriages',
  'military',
  'affordable',
];

function generate() {
  const lines = [];

  lines.push('# Domain redirects (301 Permanent) - Most specific first');
  lines.push('http://weddingcounselors.com/* https://www.weddingcounselors.com/:splat 301!');
  lines.push('https://weddingcounselors.com/* https://www.weddingcounselors.com/:splat 301!');
  lines.push('');

  lines.push('# Remove trailing slashes (301 Permanent) - except root');
  for (const pg of ['about', 'contact', 'pricing', 'features', 'support', 'guidelines', 'privacy', 'terms', 'blog', 'states']) {
    lines.push(`/${pg}/ /${pg} 301!`);
  }
  lines.push('');

  lines.push('# Handle blog post URLs specifically to prevent issues');
  lines.push('/blog/:slug/ /blog/:slug 301!');
  lines.push('');

  lines.push('# Blog slug redirects: old sitemap had wrong slugs, Google indexed them');
  lines.push('/blog/financial-questions-before-marriage /blog/financial-questions-to-ask-before-marriage 301!');
  lines.push('/blog/wedding-planning-fights /blog/fighting-about-wedding-planning 301!');
  lines.push('/blog/inlaw-boundaries /blog/setting-healthy-boundaries-with-inlaws 301!');
  lines.push('');

  lines.push('# Legacy URL redirects (301 Permanent) — consolidate duplicate paths into canonical URLs');
  lines.push('# These were previously JS-only redirects that Google crawled before the redirect fired,');
  lines.push('# creating 636+ duplicate indexed pages splitting ranking authority.');
  lines.push('/professionals/:state/:city/:slug /premarital-counseling/:state/:city/:slug 301!');
  lines.push('/professionals/:state/:city /premarital-counseling/:state/:city 301!');
  lines.push('/professionals/:state /premarital-counseling/:state 301!');
  lines.push('/professionals /premarital-counseling 301!');
  lines.push('/states/:state/:city/:slug /premarital-counseling/:state/:city/:slug 301!');
  lines.push('/states/:state/:city /premarital-counseling/:state/:city 301!');
  lines.push('/states/:state /premarital-counseling/:state 301!');
  lines.push('/states /premarital-counseling 301!');
  lines.push('');

  // --- State abbreviation redirects ---
  lines.push('# State abbreviation 301 redirects (generated by scripts/generate-redirects.js)');
  lines.push('# Fixes URL cannibalization: /premarital-counseling/al/* → /premarital-counseling/alabama/*');
  lines.push('');

  // Base: /premarital-counseling/{abbr}/* → /premarital-counseling/{full}/:splat
  lines.push('# Base state abbreviation redirects (50 × 2 = 100 rules)');
  for (const [abbr, full] of Object.entries(STATE_MAP)) {
    lines.push(`/premarital-counseling/${abbr} /premarital-counseling/${full} 301!`);
    lines.push(`/premarital-counseling/${abbr}/* /premarital-counseling/${full}/:splat 301!`);
  }
  lines.push('');

  // Specialty: /premarital-counseling/{specialty}/{abbr}/* → /{specialty}/{full}/:splat
  lines.push(`# Specialty + state abbreviation redirects (10 specialties × 50 states × 2 = 1000 rules)`);
  for (const specialty of SPECIALTIES) {
    lines.push(`# ${specialty}`);
    for (const [abbr, full] of Object.entries(STATE_MAP)) {
      lines.push(`/premarital-counseling/${specialty}/${abbr} /premarital-counseling/${specialty}/${full} 301!`);
      lines.push(`/premarital-counseling/${specialty}/${abbr}/* /premarital-counseling/${specialty}/${full}/:splat 301!`);
    }
  }
  lines.push('');

  // Discount: /premarital-counseling/marriage-license-discount/{abbr} → .../{full}
  lines.push('# Marriage license discount state abbreviation redirects (50 rules)');
  for (const [abbr, full] of Object.entries(STATE_MAP)) {
    lines.push(`/premarital-counseling/marriage-license-discount/${abbr} /premarital-counseling/marriage-license-discount/${full} 301!`);
  }
  lines.push('');

  // SPA fallback — MUST be last
  lines.push('# SPA fallback (200 OK)');
  lines.push('/* /index.html 200');

  const output = lines.join('\n') + '\n';
  const outPath = path.join(__dirname, '..', 'client', 'public', '_redirects');
  fs.writeFileSync(outPath, output, 'utf8');

  // Stats
  const ruleCount = lines.filter(l => l.match(/^\/.+ \/.+ \d+!?$/)).length;
  console.log(`Wrote ${outPath}`);
  console.log(`Total redirect rules: ${ruleCount}`);
  console.log(`  - Base state abbr:      ${Object.keys(STATE_MAP).length * 2}`);
  console.log(`  - Specialty + state:     ${SPECIALTIES.length * Object.keys(STATE_MAP).length * 2}`);
  console.log(`  - Discount state:        ${Object.keys(STATE_MAP).length}`);
  console.log(`  - Other (domain, legacy): ${ruleCount - Object.keys(STATE_MAP).length * 2 - SPECIALTIES.length * Object.keys(STATE_MAP).length * 2 - Object.keys(STATE_MAP).length}`);
}

generate();
