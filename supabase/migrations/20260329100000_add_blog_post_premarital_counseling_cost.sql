## How to Choose Based on Your Budget

Here is a practical decision framework based on what you can realistically spend:

### Under $200

- Use your EAP sessions (free)
- Take a church-based program (free–$150)
- Supplement with a self-paced online course ($20–$50)

### $200 to $600

- Find a sliding-scale therapist for 4–5 sessions
- Do a church program plus 1–2 therapist sessions (hybrid approach)
- Use an online therapist with package pricing

### $600 to $1,200

- Work with an experienced licensed therapist for 5–8 sessions
- Choose online or in-person based on preference, not cost pressure
- Include a validated assessment tool like PREPARE/ENRICH

### $1,200+

- Premium private practice with extended sessions
- Longer program (8–10 sessions) covering deeper issues
- Intensive retreat format for an immersive experience

Regardless of budget, the most important factor is **fit with your counselor**. A great therapist at $100/session will help you more than a mediocre one at $300. Ask about their approach, read reviews, and trust your gut after the first session.

