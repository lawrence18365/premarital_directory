## The Short Answer

Most couples pay **$125 to $175 per session** for premarital counseling with a licensed therapist in 2026. A typical program runs **5 sessions**, putting the total cost somewhere between **$625 and $875**.

But that range only tells part of the story. Depending on the format you choose, where you live, and whether you go through a church, your total could be anywhere from **$0 to $2,500+**.

This guide breaks down every variable so you can plan a realistic budget — and find the option that fits your situation without overpaying or cutting corners.

