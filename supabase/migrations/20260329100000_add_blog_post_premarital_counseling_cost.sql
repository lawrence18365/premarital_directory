### University Training Clinics

If you live near a university with a marriage and family therapy graduate program, their training clinic often offers couples counseling for **$10 to $50 per session**. Sessions are led by supervised graduate students who are often very well trained.

### Military Resources

Active-duty military and their partners can access free premarital counseling through Military OneSource, chaplain services, and programs like the Army's Building Strong and Ready Teams (BSRT). These are available regardless of rank or branch.

### Online Courses Under $50

Self-paced courses are not a perfect substitute for a live counselor, but a good one covers the same topics for a fraction of the cost. Look for courses built on evidence-based frameworks rather than generic advice.

