### In-Person Therapist

This is the gold standard for couples who want a tailored experience. You will sit with a licensed marriage and family therapist (LMFT), licensed professional counselor (LPC), or psychologist who specializes in couples work. Sessions are usually 50 minutes, though some premarital programs use 75- or 90-minute sessions.

Expect to pay **$150 to $250 per session** in most metro areas. Five sessions is the most common program length, though some therapists recommend 6 to 8 for couples with more to work through.

