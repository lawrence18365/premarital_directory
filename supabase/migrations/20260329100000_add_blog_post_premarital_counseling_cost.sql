## What Is Actually Included in the Cost?

Premarital counseling is not just "talking about your relationship." A well-structured program typically includes:

- **Initial assessment** — Many counselors use validated tools like [PREPARE/ENRICH](/blog/prepare-enrich-explained), [SYMBIS](/blog/symbis-explained), or [FOCCUS](/blog/foccus-explained) to identify your strengths and growth areas. The assessment itself may cost $35–$75 on top of session fees, or it may be bundled in.
- **Structured sessions** — Most programs cover communication, conflict resolution, financial planning, intimacy, family-of-origin dynamics, and expectations about roles and responsibilities.
- **Homework and exercises** — Good counselors give you things to practice between sessions. This is where the real growth happens.
- **A summary or report** — Some programs provide a written summary of your results and recommendations.
- **Follow-up check-in** — Some therapists include a follow-up session 3–6 months after the wedding at no extra charge. Ask about this when you are comparing providers.

