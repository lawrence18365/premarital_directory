## Does Insurance Cover Premarital Counseling?

The honest answer: **usually not directly**, but there are workarounds.

Most insurance plans cover therapy for diagnosed mental health conditions (anxiety, depression, adjustment disorders). Premarital counseling as "relationship education" does not typically qualify because there is no diagnosis involved.

However, here is where it gets nuanced:

- **If either partner has a diagnosable condition** (anxiety about the wedding, depression, past trauma affecting the relationship), a therapist may be able to bill insurance for individual therapy that addresses those issues within the context of your relationship.
- **Out-of-network reimbursement** — If you have a PPO plan with out-of-network benefits, you may be able to submit superbills from your counselor and get 40–70% reimbursed. Ask your insurance company about your out-of-network mental health benefits.
- **HSA / FSA funds** — Sessions with a licensed therapist (not a coach or pastor) typically qualify as an eligible expense. This effectively gives you a 25–35% discount depending on your tax bracket.

### Questions to Ask Your Insurance Company

1. Do I have out-of-network mental health benefits?
2. What is my deductible for behavioral health, and has it been met?
3. What is my reimbursement rate for out-of-network providers?
4. Does my plan cover couples therapy or only individual therapy?

