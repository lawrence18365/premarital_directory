## Free and Low-Cost Options

If budget is a real constraint, you still have solid options. Premarital counseling does not have to be expensive to be effective.

### Employee Assistance Programs (EAP)

Check whether your employer or your partner's employer offers an EAP. Many EAP programs include **3 to 6 free counseling sessions** that can be used for premarital work. This is one of the most underused benefits in the country — roughly 97% of large employers offer EAPs, but only about 5% of employees use them.

### Church and Faith-Based Programs

As mentioned above, many churches offer free or low-cost premarital programs for members and sometimes for non-members too. Even if you are not particularly religious, some congregations welcome all couples.

### Sliding Scale Therapists

Many therapists reserve a few spots on their caseload for reduced-fee clients. Rates of $50 to $80 per session are common on sliding scale. Do not be embarrassed to ask — therapists expect it.

