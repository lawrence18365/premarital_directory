## National Average Cost of Premarital Counseling

Here is what couples across the United States are actually paying in 2026:

| | Per Session | Total (5 Sessions) |
|---|---|---|
| **National average** | $125–$175 | $625–$875 |
| **Low end (sliding scale / newer therapist)** | $75–$100 | $375–$500 |
| **Mid range (experienced LMFT)** | $150–$225 | $750–$1,125 |
| **High end (premium practice / extended sessions)** | $250–$350 | $1,250–$1,750 |

These numbers reflect out-of-pocket pricing for licensed therapists and counselors. Church-based programs and online courses often cost significantly less — we will cover those below.

A few things that shift the price within these ranges:

- **Therapist credentials and experience** — an LMFT with 15 years of couples work charges more than a recently licensed counselor
- **Session length** — standard 50-minute sessions cost less than 75- or 90-minute sessions, which some premarital programs use
- **Included assessments** — programs that bundle tools like [PREPARE/ENRICH](/blog/prepare-enrich-explained) or [SYMBIS](/blog/symbis-explained) may charge a one-time assessment fee ($35–$75) on top of session rates
- **Number of sessions** — some couples need 4, others benefit from 8–10 depending on what surfaces during the process

