### Online Therapist (Video Sessions)

[Online premarital counseling](/blog/online-vs-in-person-premarital-counseling) has become the default for a lot of couples, especially when one or both partners travel for work or live in an area with limited options. Research shows outcomes are equivalent to in-person sessions for most couples.

Online sessions typically run **$100 to $200**, about 15 to 25% less than in-person, since the therapist has no office overhead. Some platforms offer package pricing in the $500 to $900 range for a full program.

### Church-Based Programs

If you are getting married in a church, your congregation may require or strongly encourage premarital counseling — and often provides it at little or no cost. Programs vary widely:

- **Pastor-led sessions** (4–6 meetings): usually free or a small materials fee
- **Catholic Pre-Cana**: $50–$150 per couple for the program, sometimes including a [FOCCUS assessment](/blog/foccus-explained)
- **Mentor couple programs**: free, facilitated by trained married couples in the congregation
- **Weekend retreats** (Engaged Encounter, etc.): $100–$300 per couple including meals

Church programs tend to emphasize values alignment, expectations, communication basics, and faith integration. If you want deeper clinical work on conflict patterns or family-of-origin issues, you might consider supplementing with a few [therapist sessions](/premarital-counseling).

### Self-Paced Online Courses

These are the most affordable option and work well as a supplement to other counseling — or as a standalone for couples who are strong communicators and want a structured framework.

Prices range from **$20 to $150** for a one-time purchase. Most include video lessons, worksheets, and sometimes a basic compatibility assessment. They lack the personalized feedback of a live counselor, but the best ones are built on the same research.

