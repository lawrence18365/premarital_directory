## What to ask a SYMBIS facilitator before you start

1. **How many debrief sessions do you do?** (1 is common but 2–3 is better)
2. **Do you do any follow-up sessions beyond the SYMBIS results?**
3. **How do you handle areas where we have major differences?**
4. **Do you supplement SYMBIS with other exercises or curriculum?**
5. **What''s your background?** (pastor, licensed counselor, certified coach, mentor couple)

