## Gottman Training Levels Explained

The Gottman Institute offers several levels of training, and they are *not* equivalent. When a therapist says they''re "Gottman-trained," it can mean very different things:

**Level 1 Training:** A 2-day workshop introducing the Gottman Method. Thousands of therapists have completed this. It''s a meaningful starting point but represents minimal expertise.

