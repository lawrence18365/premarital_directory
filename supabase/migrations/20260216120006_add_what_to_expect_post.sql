## Common questions

**Do we both have to go?**
Yes. Premarital counseling is a couples process. Both partners need to be there.

**What if one of us doesn''t want to go?**
This is common. Often the reluctant partner warms up after the first session when they realize it''s not about being criticized — it''s about building something together.

**What if we start crying or fighting in session?**
That''s OK. Your counselor is trained for this. Emotional moments are often the most productive ones.

**Is everything we say confidential?**
Yes, with standard exceptions (danger to self or others). Your counselor will explain their confidentiality policy in the first session.

**Can we do it online?**
Absolutely. Many counselors offer video sessions, and the research shows online therapy is comparably effective. [Find online premarital counselors →](/premarital-counseling/online)

