## How Much Does Catholic Marriage Preparation Cost?

**Pre-Cana:** Many parish programs charge $50–$200 for a couples retreat or class series. Some dioceses offer free programs. The FOCCUS inventory itself typically costs $30–$50. If cost is a barrier, speak with your parish priest — financial assistance is usually available.

**Private Catholic counseling:** Rates are similar to other licensed therapy — $100–$200 per session. Some Catholic Charities agencies offer sliding-scale counseling for engaged couples.

## Frequently Asked Questions

**Can we do online Pre-Cana?**
Some dioceses accept online or distance-learning Pre-Cana programs, especially for couples who live in different cities or have scheduling constraints. Confirm with your diocese before enrolling — not all dioceses accept online programs as fulfilling the requirement.

**What if one of us isn''t Catholic?**
Interfaith couples (one Catholic, one non-Catholic) can still marry in the Catholic Church. The Catholic partner typically still completes Pre-Cana; interfaith-specific sessions may be added. The non-Catholic partner is not required to convert or complete the same formation.

**Is Pre-Cana confidential?**
Yes. Content shared in Pre-Cana — whether in group sessions or with a sponsor couple — is treated with confidentiality. The parish receives only confirmation that you completed the program, not details of what was discussed.

**Does completing Pre-Cana count for a state marriage license discount?**
In most states that offer marriage license discounts for premarital counseling, Pre-Cana qualifies — as long as a licensed professional or qualifying clergy member can sign the required documentation. Confirm with your specific Pre-Cana facilitator whether they can provide a certificate for your state.
